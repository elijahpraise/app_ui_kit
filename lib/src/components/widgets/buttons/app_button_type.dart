/// Visual style variants for app buttons.
///
/// Each value maps to a different color scheme from the current [ColorScheme].
enum AppButtonType {
  /// The primary action style, typically using [ColorScheme.primary].
  primary,
  /// The secondary action style, typically using [ColorScheme.secondary].
  secondary,
  /// The error/destructive action style, typically using [ColorScheme.error].
  error,
}
